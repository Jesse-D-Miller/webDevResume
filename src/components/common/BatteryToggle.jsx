function BatteryToggle({ onClick, theme }) {
  const defaultIndex = theme === "dark" ? 0 : 7;
  const [frame, setFrame] = useState(defaultIndex);
  const intervalRef = useRef(null);

  useEffect(() => {
    setFrame(defaultIndex);
  }, [defaultIndex]);

  const startAnimation = () => {
    if (intervalRef.current) return;
    if (theme === "cyber") return;

    let i = frame;
    intervalRef.current = setInterval(() => {
      setFrame(i);
      i = (i + 1) % BatteryLevels.length;
    }, 500);
  };

  const stopAnimation = () => {
    if (!intervalRef.current) return;
    clearInterval(intervalRef.current);
    intervalRef.current = null;
  };

  return (
    <div
      className="battery-toggle"
      onMouseEnter={startAnimation}
      onMouseLeave={stopAnimation}
      onClick={(e) => {
        stopAnimation();
        onClick(e);
      }}
    >
      {BatteryLevels[frame]}
    </div>
  );
}

export default BatteryToggle;
